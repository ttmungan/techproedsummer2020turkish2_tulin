package day04variables;

public class Day04_homework3 {

	public static void main(String[] args) {
		
		float num1 = 3.23f; //To solve the problem we should add an f to the number  
		double num2 =3.23;
		System.out.println(num1);
		System.out.println(num2);
		
		float number1= (float)3.23; //We can add a float word before the number.
		double number2= 3.23; 
		System.out.println(number1);
		System.out.println(number2);
		
		float number3= 3.23f;
		double number4= (double)3.23;
		System.out.println(number3);
		System.out.println(number4);
		
		
		

	}

}
